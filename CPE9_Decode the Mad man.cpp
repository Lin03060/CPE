#include <iostream>
#include <unordered_map>
#include <string>
using namespace std;

int main() {
    unordered_map<char, char> decode = {
        {'1','`'},{'2','1'},{'3','2'},{'4','3'},{'5','4'},{'6','5'},{'7','6'},{'8','7'},{'9','8'},{'0','9'},
        {'-','0'},{'=','-'},{'W','Q'},{'E','W'},{'R','E'},{'T','R'},{'Y','T'},{'U','Y'},{'I','U'},{'O','I'},
        {'P','O'},{'[','P'},{']','['},{'\\',']'},{'S','A'},{'D','S'},{'F','D'},{'G','F'},{'H','G'},{'J','H'},
        {'K','J'},{'L','K'},{';','L'},{'\'',';'},{'X','Z'},{'C','X'},{'V','C'},{'B','V'},{'N','B'},{'M','N'},
        {',','M'},{'.',','},{'/','.'},{'w','q'},{'e','w'},{'r','e'},{'t','r'},{'y','t'},{'u','y'},{'i','u'},
        {'o','i'},{'p','o'},{'[','p'},{']','['},{'s','a'},{'d','s'},{'f','d'},{'g','f'},{'h','g'},{'j','h'},
        {'k','j'},{'l','k'},{';','l'},{'\'',';'},{'x','z'},{'c','x'},{'v','c'},{'b','v'},{'n','b'},{'m','n'},
        {',','m'},{'.',','},{'/','.'}
    };

    string line;
    while (getline(cin, line)) {
        for (char c : line) {
            if (c == ' ') cout << ' ';
            else if (decode.count(c)) cout << decode[c];
            else cout << c; 
        }
        cout << endl;
    }
    return 0;
}

